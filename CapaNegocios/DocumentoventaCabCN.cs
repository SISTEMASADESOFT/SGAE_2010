        public DocumentoVentaCabCE F_DOCUMENTOVENTACAB_COMPRA_EXTERNA(DocumentoVentaCabCE objEntidad)
        {
            try
            {
                DataTable dtDatos = obj.F_DOCUMENTOVENTACAB_COMPRA_EXTERNA(objEntidad);
                if (dtDatos.Rows.Count > 0)
                {
                    try
                    {
                        foreach (DataRow i in dtDatos.Rows)
                        {
                            objEntidad.CodDocumentoVenta = int.Parse(i["CodDocumentoVenta"].ToString());
                            objEntidad.SerieDoc = i["SerieDoc"].ToString();
                            objEntidad.NumeroDoc = i["NumeroDoc"].ToString();
                            objEntidad.FechaEmision = DateTime.Parse(i["Emision"].ToString());
                            objEntidad.EmisionStr = objEntidad.FechaEmision.ToString();
                            objEntidad.Vencimiento = DateTime.Parse(i["Vencimiento"].ToString());
                            objEntidad.VencimientoStr = objEntidad.Vencimiento.ToString();

                            objEntidad.ListaDocumentoVentaDet = new List<DocumentoVentaCabCE>();

                            dtDatos = (new DocumentoVentaCabCN()).F_DOCUMENTOVENTAB_COMPRA_EXTERNA_DETALLE(objEntidad);
                            if (dtDatos.Rows.Count > 0)
                            {
                                foreach (DataRow r in dtDatos.Rows)
                                {
                                    objEntidad.ListaDocumentoVentaDet.Add(new DocumentoVentaCabCE()
                                    {
                                        Precio = decimal.Parse(r["Precio"].ToString()),
                                        CodProducto = int.Parse(r["CodProducto"].ToString()),
                                        Cantidad = decimal.Parse(r["Cantidad"].ToString()),
                                        Descripcion = r["Descripcion"].ToString(),
                                        CodUnidadVenta = int.Parse(r["CodUnidadVenta"].ToString()),
                                        CodigoInterno = r["CodigoInterno"].ToString(),
                                    });
                                }
